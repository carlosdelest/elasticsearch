package co.elastic.elasticsearch.serverless.multiproject.action;

import co.elastic.elasticsearch.serverless.multiproject.action.TransportGetProjectStatusAction.Request;

import org.elasticsearch.common.io.stream.Writeable;
import org.elasticsearch.test.AbstractWireSerializingTestCase;
import org.elasticsearch.test.ESTestCase;

import java.io.IOException;

public class GetProjectStatusRequestSerializationTests extends AbstractWireSerializingTestCase<Request> {

    @Override
    protected Writeable.Reader<Request> instanceReader() {
        return Request::new;
    }

    @Override
    protected Request createTestInstance() {
        return new Request(TEST_REQUEST_TIMEOUT, randomUUID());
    }

    @Override
    protected Request mutateInstance(Request instance) throws IOException {
        return new Request(instance.masterNodeTimeout(), randomValueOtherThan(instance.getProjectId(), ESTestCase::randomUUID));
    }
}
